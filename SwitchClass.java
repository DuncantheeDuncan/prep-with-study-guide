/**
 * SwitchClass
 */
public class SwitchClass {

    public static void main(String[] args) {
        switch (5) { 
            
            case 0: 
               System.out.println("zero"); 
            //    break; 
            case 1: 
               System.out.println("one"); 
               case 4: 
               System.out.println("joe"); 
               default: 
               System.out.println("default"); 
            //    break;
            case 2: 
               System.out.println("two"); 
        } 

        
    }
}